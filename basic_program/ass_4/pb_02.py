# WAP to demonstrate use of assignment operators (=, +=, -=, /=, *=, %=)

# using numeric
#use of =
a = int(input("Enter the first number : "))
b = int(input("Enter the second number : "))

# use of +=
a += b    # in this value of b is added in a
print(a)

# use of -=
a -= b    # in this value of a is subtract from value of b
print(a)

# use of *=
c = 5
c *= 5
print(c)

# use of /=
d = 110
d /= 10
print(d)

# use of %=
e = 111
e %= 10
print(e)

# using string (in string it assign value to the string and concatenate string)
x = input("Enter the first string : ")
y = input("Enter the second string : ")

# use +=
x += y
print(x)

# use *=
x *= 3
print(x)

