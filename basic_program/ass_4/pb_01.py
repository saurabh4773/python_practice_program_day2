# WAP to demonstrate use of arithmatic operators (+, -, /, *, %, **)

# using numeric values
a = int(input("Enter the first number : "))
b = int(input("Enter the second number : "))

# use of + (it gives addition of two numbers)
print("Addition",a+b)
# use of - (it gives subtraction of two numbers)
print("Subtraction",a-b)
# use of / (it gives division of two number in float)
print("Division",a/b)
# use of * (it gives multiplication of two numbers)
print("Multiplication",a*b)
# use of % (it gives remainder after division of two numbers)
print("Remainder",a%b)
#use of ** (it is use for to calculate power)
print("Exponential",a**b)

# operators for string object
str1 = input("Enter the first string : ")
str2 = input("Enter the second string : ")

# use of + (used for concatenation)
print(str1+str2)
# use of * (use to print string mutilple times)
print(str1*3)
